package Preparetion1;

public class WhileLoop {

    public static void main(String[] args) {


        int i = 10;
        while (i > 0) {

            System.out.println(i);
            i--;
        }

        int j = 20;
        do {
            System.out.println(j);
            j++;
        } while (j < 30);
    }
}
